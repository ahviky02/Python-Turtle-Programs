# List Operation in Python
l = [43, 21, 34, 78, 9]

# check Data type
print("Type =", type(l))

# sort a list
l.sort()
print("Sort =", l)

# reverse of a list
l.reverse()
print("Reverse =", l)

# pop element from list
l.pop(2)
print("Pop =", l)

# insert a element
l.insert(2, 333)
print("Insert =", l)

 
